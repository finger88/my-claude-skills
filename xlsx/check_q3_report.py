issues = []
location_issues = []

for i, q3 in enumerate(q3_projects):
    table = df.iloc[i]

    # 1. 核对面积
    area_diff = abs(q3['面积'] - table['方式面积'])
    if area_diff >= 0.001:
        issues.append({
            '序号': i+1,
            '编号': table['编号'],
            '字段': '宗海面积',
            'Q3报告': f"{q3['面积']}公顷",
            '表格': f"{table['方式面积']:.4f}公顷",
            '差异': f"{area_diff:.4f}公顷"
        })

    # 2. 核对用海方式
    q3_way = q3['用海方式'].replace('和', '、')
    table_way = table['用海方式']

    key_terms = ['建设填海造地', '透水构筑物', '非透水构筑物', '港池', '取、排水口']
    for term in key_terms:
        if term in q3_way and term not in table_way:
            issues.append({
                '序号': i+1,
                '编号': table['编号'],
                '字段': '用海方式',
                'Q3报告': q3['用海方式'],
                '表格': table_way,
                '差异': f'缺少{term}'
            })
            break

    # 3. 核对起始时间
    table_start = pd.to_datetime(table['开始时间']).strftime('%Y-%m-%d') if pd.notna(table['开始时间']) else ''
    if q3['起始时间'] != table_start:
        issues.append({
            '序号': i+1,
            '编号': table['编号'],
            '字段': '起始时间',
            'Q3报告': q3['起始时间'],
            '表格': table_start,
            '差异': '不一致'
        })

    # 4. 核对终止时间
    table_end = pd.to_datetime(table['终止时间']).strftime('%Y-%m-%d') if pd.notna(table['终止时间']) else ''
    if q3['终止时间'] != table_end:
        issues.append({
            '序号': i+1,
            '编号': table['编号'],
            '字段': '终止时间',
            'Q3报告': q3['终止时间'],
            '表格': table_end,
            '差异': '不一致'
        })

    # 5. 核对位置
    q3_loc = q3['位置']
    table_loc = table['位置说明'] if pd.notna(table['位置说明']) else ''

    match = False
    if q3_loc[:5] in table_loc or q3_loc[-5:] in table_loc:
        match = True
    else:
        keywords = ['洞头', '灵昆', '瓯江', '状元岙', '霓屿', '大门', '龙湾']
        for kw in keywords:
            if kw in q3_loc and kw in table_loc:
                match = True
                break

    if not match:
        location_issues.append({
            '序号': i+1,
            '编号': table['编号'],
            '项目名称': q3['项目名称'],
            'Q3位置': q3_loc,
            '表格位置': table_loc[:50] if table_loc else '空'
        })

