issues = []
for i, q4 in enumerate(q4_projects):
    table = df.iloc[i]

    # 1. 核对面积
    area_diff = abs(q4['面积'] - table['方式面积'])
    if area_diff >= 0.001:
        issues.append({
            '序号': i+1,
            '编号': table['编号'],
            '字段': '宗海面积',
            'Q4报告': f"{q4['面积']}公顷",
            '表格': f"{table['方式面积']:.4f}公顷",
            '差异': f"{area_diff:.4f}公顷"
        })

    # 2. 核对终止时间
    table_end = pd.to_datetime(table['终止时间']).strftime('%Y-%m-%d') if pd.notna(table['终止时间']) else ''
