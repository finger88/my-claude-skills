issues = []
for i, q2 in enumerate(q2_projects):
    table = df.iloc[i]

    # 检查面积差异
    area_diff = abs(q2['宗海面积'] - table['方式面积'])
    if area_diff >= 0.001:  # 0.001公顷以上算差异
        issues.append({
            '序号': i+1,
            '编号': table['编号'],
            '项目名称': q2['项目名称'],
            '问题': '面积差异',
            'Q2报告': f"{q2['宗海面积']}公顷",
            '表格': f"{table['方式面积']:.4f}公顷",
            '差异': f"{area_diff:.4f}公顷"
        })

    # 检查用海方式差异
    q2_way = q2['用海方式'].replace('和', '、')
    table_way = table['用海方式']

    # 检查关键词
    key_terms = ['建设填海造地', '透水构筑物', '非透水构筑物', '港池', '取、排水口']
    for term in key_terms:
        if term in q2_way and term not in table_way:
            issues.append({
                '序号': i+1,
                '编号': table['编号'],
                '项目名称': q2['项目名称'],
                '问题': '用海方式差异',
                'Q2报告': q2['用海方式'],
                '表格': table['用海方式'],
                '差异': f'缺少{term}'
            })
            break

